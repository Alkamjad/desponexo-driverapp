// Aktuelle Sprache aus localStorage
const getCurrentLanguage = () => {
  return localStorage.getItem('app_language') || 'de';
};

// Sprache setzen
const setLanguage = (lang) => {
  localStorage.setItem('app_language', lang);
  
  // RTL für Arabisch
  if (lang === 'ar') {
    document.documentElement.setAttribute('dir', 'rtl');
    document.documentElement.setAttribute('lang', 'ar');
  } else {
    document.documentElement.setAttribute('dir', 'ltr');
    document.documentElement.setAttribute('lang', 'de');
  }
  
  // Seite neu laden für vollständige Aktualisierung
  window.location.reload();
};

// Translation Funktion
const t = (key) => {
  const lang = getCurrentLanguage();
  return translations[lang]?.[key] || translations['de'][key] || key;
};

// Check ob RTL (Right-to-Left)
const isRTL = () => {
  return getCurrentLanguage() === 'ar';
};

// Initialize Language on App Start
const initLanguage = () => {
  const lang = getCurrentLanguage();
  if (lang === 'ar') {
    document.documentElement.setAttribute('dir', 'rtl');
    document.documentElement.setAttribute('lang', 'ar');
  } else {
    document.documentElement.setAttribute('dir', 'ltr');
    document.documentElement.setAttribute('lang', lang);
  }
};

export { t, setLanguage, isRTL, getCurrentLanguage, initLanguage };